a = 10
b = 3

# Arithmetic
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison
print("Equal:", a == b)
print("Not equal:", a != b)
print("Greater:", a > b)
print("Less:", a < b)

# Logical
x = True
y = False
print("AND:", x and y)
print("OR:", x or y)
print("NOT x:", not x)

# Bitwise
print("Bitwise AND:", a & b)
print("Bitwise OR:", a | b)
print("Bitwise XOR:", a ^ b)
print("Bitwise NOT a:", ~a)
print("Left shift:", a << 1)
print("Right shift:", a >> 1)
